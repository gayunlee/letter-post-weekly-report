def has_match(text, patterns):
    for p in patterns:
        if p.search(text):
            return True
    return False


def count_matches(text, patterns):
    count = 0
    for p in patterns:
        if p.search(text):
            count += 1
    return count


def classify_v3c(text):
    """v3c 우선순위 기반 분류"""
    # 5순위: 기타 (텍스트 3자 미만 또는 비어있음)
    if not text or len(text.strip()) < 3:
        return "기타"

    cleaned = text.strip()

    # 무의미 노이즈 체크
    if re.match(r'^[\s\.\?\!\~\-\+\=ㅋㅎㅠㅜㅡ…·•♡♥💗💜❤️🫶🏻🙏💕😭😁😂🤣😄☺️🤨😢🥲🫡⭐️🖤👍🏻🔺]+$', cleaned):
        return "기타"
    if re.match(r'^[0-9\s]+$', cleaned):
        return "기타"
    if re.match(r'^[ㄱ-ㅎ\s]+$', cleaned):
        return "기타"
    # 2글자 이하의 무의미
    if len(cleaned) <= 2:
        return "기타"

    inv_count = count_matches(cleaned, INV_PATTERNS)

    # ── 1순위: 운영 피드백 ──
    # 강한 운영 패턴: 투자 신호 수와 무관하게 운영 피드백
    if has_match(cleaned, STRONG_OP_PATTERNS):
        # 단, "환불"이 있지만 전체 맥락이 투자 불만인 경우도 운영 피드백으로 본다
        # (환불은 운영팀이 처리하므로)
        # 예외: 투자 내용이 아주 압도적이고 환불은 부수적 언급인 경우
        # -> 그래도 환불이 있으면 운영 피드백 (운영팀 액션 아이템)
        return "운영 피드백"

    # 약한 운영 패턴: 투자 신호가 적으면 운영 피드백
    if has_match(cleaned, WEAK_OP_PATTERNS):
        # "해지" 단독 언급이지만 투자 내용이 주인 경우 -> 콘텐츠·투자
        # "영상 기다립니다" 같은 단순 요청도 투자 맥락이면 콘텐츠·투자
        if inv_count <= 2:
            return "운영 피드백"
        # 투자 신호가 강하면 콘텐츠·투자로 넘김

    # ── 2순위: 서비스 피드백 ──
    if has_match(cleaned, SVC_PATTERNS):
        if inv_count <= 2:
            return "서비스 피드백"
        # 투자 내용이 주이면 콘텐츠·투자

    # ── 3순위: 콘텐츠·투자 ──
    if inv_count > 0:
        return "콘텐츠·투자"

    # ── 4순위: 일상·감사 ──
    if has_match(cleaned, DAILY_PATTERNS):
        return "일상·감사"

    # ── 5순위: 기타 또는 기본값 ──
    if len(cleaned) < 10:
        return "기타"

    # 투자 교육 커뮤니티 맥락에서 애매한 글은 콘텐츠·투자
    return "콘텐츠·투자"


def main():
    with open(INPUT_PATH, "r", encoding="utf-8") as f:
        data = json.load(f)

    print(f"총 항목 수: {len(data)}")

    # 분류 수행
    counts = {}
    for item in data:
        text = item.get("text", "")
        topic = classify_v3c(text)
        item["v3c_topic"] = topic
        counts[topic] = counts.get(topic, 0) + 1

    # 결과 저장
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"\n분류 결과:")
    for topic, count in sorted(counts.items(), key=lambda x: -x[1]):
        print(f"  {topic}: {count}건 ({count/len(data)*100:.1f}%)")
    print(f"\n총: {sum(counts.values())}건")
    print(f"출력 파일: {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
