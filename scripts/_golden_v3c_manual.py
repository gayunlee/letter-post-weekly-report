# 변경 사항 출력
changes_from_v4 = []
for i, item in enumerate(golden):
    v4 = item.get('v4_topic', '')
    v3c = item['v3c_topic']
    if v4 != v3c:
        text_short = item['text'][:50].replace('\n', ' ')
        changes_from_v4.append((i, v4, v3c, text_short))

