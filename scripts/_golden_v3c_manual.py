for idx, old, new, text in changes_from_v4:
    print(f'  [{idx:3d}] {old:8s} -> {new:8s} | {text}')

# 분포
dist = Counter(item['v3c_topic'] for item in golden)
print(f'\nGolden v3c 최종 분포:')
