"""부서 자동 라우팅

Topic × Intent 조합에 따라 담당 부서를 자동 매핑합니다.
"""
from typing import List, Dict, Tuple, Optional


# ── 라우팅 매핑 테이블 ──────────────────────────────────────────
# (topic, intent) → [부서, ...]
# intent가 "*"인 항목은 해당 topic의 기본 라우팅
ROUTING_TABLE: Dict[Tuple[str, str], List[str]] = {
    ("서비스 이슈", "제보/건의"):   ["개발팀"],
    ("서비스 이슈", "질문/요청"):   ["CS팀", "개발팀"],
    ("서비스 이슈", "피드백/의견"): ["운영팀"],
    ("서비스 이슈", "정보공유"):    ["운영팀"],
    ("콘텐츠 반응", "질문/요청"):   ["콘텐츠팀"],
    ("콘텐츠 반응", "피드백/의견"): ["콘텐츠팀", "사업팀"],
    ("콘텐츠 반응", "제보/건의"):   ["콘텐츠팀"],
    ("콘텐츠 반응", "정보공유"):    ["콘텐츠팀"],
    ("투자 이야기", "질문/요청"):   ["콘텐츠팀"],
    ("투자 이야기", "피드백/의견"): ["사업팀"],
    ("투자 이야기", "제보/건의"):   ["콘텐츠팀"],
    ("투자 이야기", "정보공유"):    ["콘텐츠팀"],
    ("커뮤니티 소통", "질문/요청"):   ["운영팀"],
    ("커뮤니티 소통", "피드백/의견"): ["운영팀"],
    ("커뮤니티 소통", "제보/건의"):   ["운영팀"],
    ("커뮤니티 소통", "정보공유"):    ["운영팀"],
}

# topic별 기본 라우팅 (intent가 매핑에 없을 때)
DEFAULT_ROUTES: Dict[str, List[str]] = {
    "서비스 이슈": ["운영팀"],
    "콘텐츠 반응": ["콘텐츠팀"],
    "투자 이야기": ["콘텐츠팀"],
    "커뮤니티 소통": ["운영팀"],
}


def route_department(topic: str, intent: str) -> List[str]:
    """Topic × Intent 조합으로 담당 부서 결정

    Args:
        topic: 주제 분류 결과
        intent: 의도 분류 결과

    Returns:
        담당 부서 리스트
    """
    route = ROUTING_TABLE.get((topic, intent))
    if route:
        return route

    return DEFAULT_ROUTES.get(topic, ["운영팀"])
